package restaurant1;

public class SalesBean {
	private int sales_num;
	private int sales_menu;
	private int sales_amount;
	private int sales_price;
	private String sales_at;
	private int sales_total_price;
	
	public int getSales_num() {
		return sales_num;
	}
	public void setSales_num(int sales_num) {
		this.sales_num = sales_num;
	}
	public int getSales_menu() {
		return sales_menu;
	}
	public void setSales_menu(int sales_menu) {
		this.sales_menu = sales_menu;
	}
	public int getSales_amount() {
		return sales_amount;
	}
	public void setSales_amount(int sales_amount) {
		this.sales_amount = sales_amount;
	}
	public int getSales_price() {
		return sales_price;
	}
	public void setSales_price(int sales_price) {
		this.sales_price = sales_price;
	}
	public String getSales_at() {
		return sales_at;
	}
	public void setSales_at(String sales_at) {
		this.sales_at = sales_at;
	}
	public int getSales_total_price() {
		return sales_total_price;
	}
	public void setSales_total_price(int sales_total_price) {
		this.sales_total_price = sales_total_price;
	}
	
	
}
